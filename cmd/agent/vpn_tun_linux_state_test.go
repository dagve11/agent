func TestParseLinuxRouteLines(t *testing.T) {
	raw := `default via 192.168.1.1 dev eth0 proto dhcp metric 100
10.0.0.0/8 dev nezha-vpn scope link
172.19.0.0/30 dev nezha-vpn proto kernel scope link src 172.19.0.1
192.168.1.0/24 dev eth0 proto kernel scope link src 192.168.1.10 metric 100`

	got := parseLinuxRouteLines(raw)
	want := []linuxVPNTunRoute{
		{Prefix: "default", Dev: "eth0", Raw: "default via 192.168.1.1 dev eth0 proto dhcp metric 100"},
		{Prefix: "10.0.0.0/8", Dev: "nezha-vpn", Raw: "10.0.0.0/8 dev nezha-vpn scope link"},
		{Prefix: "172.19.0.0/30", Dev: "nezha-vpn", Raw: "172.19.0.0/30 dev nezha-vpn proto kernel scope link src 172.19.0.1"},
		{Prefix: "192.168.1.0/24", Dev: "eth0", Raw: "192.168.1.0/24 dev eth0 proto kernel scope link src 192.168.1.10 metric 100"},
	}
	if !reflect.DeepEqual(got, want) {
		t.Fatalf("linux routes mismatch:\nwant %#v\ngot  %#v", want, got)
	}
}

func TestBuildLinuxRouteCleanupCommandsForTunInterface(t *testing.T) {
	before := `default via 192.168.1.1 dev eth0 proto dhcp metric 100
192.168.1.0/24 dev eth0 proto kernel scope link src 192.168.1.10 metric 100`
	after := `default dev nezha-vpn scope link
10.0.0.0/8 dev nezha-vpn scope link
default via 192.168.1.1 dev eth0 proto dhcp metric 100
192.168.1.0/24 dev eth0 proto kernel scope link src 192.168.1.10 metric 100`

	got := buildLinuxRouteCleanupCommands("nezha-vpn", before, after)
	want := []vpnTunCommand{
		{Name: "ip", Args: []string{"route", "del", "default", "dev", "nezha-vpn"}},
		{Name: "ip", Args: []string{"route", "del", "10.0.0.0/8", "dev", "nezha-vpn"}},
	}
	if !reflect.DeepEqual(got, want) {
		t.Fatalf("linux route cleanup commands mismatch:\nwant %#v\ngot  %#v", want, got)
	}
}

