func TestParseLinuxResolvedLinkDNSState(t *testing.T) {
	raw := `Global
         Protocols: -LLMNR -mDNS -DNSOverTLS DNSSEC=no/unsupported
  resolv.conf mode: stub

Link 2 (eth0)
    Current Scopes: DNS
         Protocols: +DefaultRoute -LLMNR -mDNS -DNSOverTLS DNSSEC=no/unsupported
Current DNS Server: 192.168.1.1
       DNS Servers: 192.168.1.1 8.8.8.8
        DNS Domain: lan ~corp.example

Link 8 (nezha-vpn)
    Current Scopes: DNS
         Protocols: -DefaultRoute -LLMNR -mDNS -DNSOverTLS DNSSEC=no/unsupported
Current DNS Server: 1.1.1.1
       DNS Servers: 1.1.1.1
        DNS Domain: ~.`

	got := parseLinuxResolvedLinkDNSState(raw)
	want := []vpnTunDNSState{
		{
			Source:    "systemd-resolved",
			Interface: "eth0",
			Servers:   []string{"192.168.1.1", "8.8.8.8"},
			Domains:   []string{"lan", "~corp.example"},
			Raw:       "default_route=true",
		},
		{
			Source:    "systemd-resolved",
			Interface: "nezha-vpn",
			Servers:   []string{"1.1.1.1"},
			Domains:   []string{"~."},
			Raw:       "default_route=false",
		},
	}
	if !reflect.DeepEqual(got, want) {
		t.Fatalf("resolved link DNS state mismatch:\nwant %#v\ngot  %#v", want, got)
	}
}

func TestBuildLinuxResolvedDNSRestoreCommands(t *testing.T) {
	before := []vpnTunDNSState{
		{
			Source:    "systemd-resolved",
			Interface: "eth0",
			Servers:   []string{"192.168.1.1", "8.8.8.8"},
			Domains:   []string{"lan", "~corp.example"},
			Raw:       "default_route=true",
		},
	}
	afterRaw := `Link 2 (eth0)
         Protocols: -DefaultRoute -LLMNR -mDNS -DNSOverTLS DNSSEC=no/unsupported
       DNS Servers: 1.1.1.1
        DNS Domain: ~.

Link 8 (nezha-vpn)
         Protocols: -DefaultRoute -LLMNR -mDNS -DNSOverTLS DNSSEC=no/unsupported
       DNS Servers: 10.0.0.53
        DNS Domain: ~.`

	got := buildLinuxResolvedDNSRestoreCommands(before, afterRaw, "nezha-vpn")
	want := []vpnTunCommand{
		{Name: "resolvectl", Args: []string{"revert", "nezha-vpn"}},
		{Name: "resolvectl", Args: []string{"revert", "eth0"}},
		{Name: "resolvectl", Args: []string{"dns", "eth0", "192.168.1.1", "8.8.8.8"}},
		{Name: "resolvectl", Args: []string{"domain", "eth0", "lan", "~corp.example"}},
		{Name: "resolvectl", Args: []string{"default-route", "eth0", "yes"}},
	}
	if !reflect.DeepEqual(got, want) {
		t.Fatalf("resolved DNS restore commands mismatch:\nwant %#v\ngot  %#v", want, got)
	}
}
